def NoSeats(seats,students):
  students.sort()
  seats.sort()
  ans=0
  for i in range(len(seats)):
    ans+=abs(seats[i]-students[i])  #abs used to give absolute of a value 
    # a=-15 --> abs(a)=15
  return ans

seats=int(input(" "))
students=int(input(" "))
print("output",NoSeats(seats,students))

